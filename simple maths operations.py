# exponentiation
num1 = 2
num2 = 4
num3 = 5
print(pow(num1, num2))

import math
print(math.pow(num1, num2))

import operator
print(operator.pow(num1, num2))

print(pow(num1, num2, num3))

# special functions
import math
print(math.sqrt(num2))

import cmath
print(cmath.sqrt(num2))

# finding a root of a number cube root
x = 8
import math
print(math.pow(x, 1/3))

# bitwise operations %,|,^,>>,<<
# left shift <<  of 1is like  multiplying by 2
print(4 << 2)
